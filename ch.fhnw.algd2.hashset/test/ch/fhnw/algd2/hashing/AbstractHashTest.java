	protected final String[] getNonLetterStringsWithDifferentHash(int number) {
		if (number > 1000)
			throw new IllegalArgumentException("Number must be less than 1000");
		String[] array = new String[number];
		for (int i = 0; i < number; i++) {
			array[i] = String.valueOf(125 + i);
		}
		checkDifferentHashes(array);
		return array;
	}

	private void checkDifferentHashes(String[] array) {
		Set<Integer> set = new HashSet<Integer>();
		for (int i = 0; i < array.length; i++) {
			if (!set.add(array[i].hashCode())) {
				throw new IllegalStateException("Hashes are not unique");
			}
		}
	}

	private void checkSameHashes(String[] words) {
		if (words.length == 0)
			throw new IllegalStateException(
					"Number of strings must be lager than 0");
		int hash = words[0].hashCode();
		for (int i = 1; i < words.length; i++) {
			assertEquals(hash, words[i].hashCode());
		}
	}
